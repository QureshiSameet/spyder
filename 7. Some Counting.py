# Loop count up from 0 to 50 in increments of 1
for a in range(51):
    print(a)

# Loop count down from 50 to 0 in decrement of 1
for a in range(50, -1, -1):
    print(a)

# Loop count up from 30 to 50 in increments of 1
for a in range(30, 51):
    print(a)

# Loop count down from 50 to 10 in decrements of 2
for a in range(50, 9, -2):
    print(a)

# Loop count up from 100 to 200 in increments of 5
for a in range(100, 201, 5):
    print(a)
